    private void jButton_ModificarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ModificarRegistroActionPerformed
        // Bot√≥n Modificar
        String idRegistro = jTextField_ID.getText();
        Boolean idFound = false;
        
        try {
            conn = main.Enlace(conn); // Conexi√≥n
            rs = main.EnlEst(rs, nombreTabla); //Query
            ResultSetMetaData rsMd = rs.getMetaData(); //Donde guardamos los resultados
            int cantidadColumnas = rsMd.getColumnCount();
            for (int i=1; i <= cantidadColumnas; i++ ){
                modelo.addColumn(rsMd.getColumnName(i));
            }
            while(rs.next()){
                Object[] fila = new Object[cantidadColumnas];
                for (int i=0; i < cantidadColumnas; i++ ){
                    fila[i] = rs.getObject(1);
                    if(i == 0){
                        //Para posicionarse en la columna de los IDs
                        //System.out.println("Item: " + rs.getObject(1).toString());
                        if (rs.getObject(1).toString().equals(idRegistro) ) {
                            idFound = true;
                            System.out.println("¬°ID  para modificar encontrado!");
                    }
                    }
                }
                modelo.addRow(fila);
            }
            int cantFilas = modelo.getRowCount();
            rs.close();
            conn.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
        if (idFound == true){
            //Realizar la modificaci√≥n
            
            try {
                String argsParaEliminar = "";
                conn = main.Enlace(conn);
                
                if(!(jTextField_Nombre.getText().equals(""))){
                    //Agregar a String de eliminaci√≥n el registro Nombre
                    argsParaEliminar = argsParaEliminar + "NOMBRECURSO = '" + jTextField_Nombre.getText() + "', ";
                }
                if(!(jTextField_Salon.getText().equals(""))){
                    //Agregar a String de eliminaci√≥n el registro SALON CURSO
                    argsParaEliminar = argsParaEliminar + "SALONCURSO = '" + jTextField_Salon.getText() + "', ";
                }
                if(!(jTextField_Grado.getText().equals(""))){
                    //Agregar a String de eliminaci√≥n el registro GRADO CURSO
                    argsParaEliminar = argsParaEliminar + "GRADOCURSO = '" + jTextField_Grado.getText() + "', ";
                }
                if(!(jTextField_Horario.getText().equals(""))){
                    //Agregar a String de eliminaci√≥n el registro HORARIO CURSO
                    argsParaEliminar = argsParaEliminar + "HORARIOCURSO = '" + jTextField_Horario.getText() + "', ";
                }
                if(!(jTextField_IDMaestro.getText().equals(""))){
                    //Agregar a String de eliminaci√≥n el registro Sexo
                    argsParaEliminar = argsParaEliminar + "IDMaestro = '" + jTextField_IDMaestro.getText() + "', ";
                }
                
                if(!(argsParaEliminar.equals(""))){
                    // Hay argumentos para eliminar
                    String sqlModificar = "";
                    StringBuilder argsParaEliminar_Mod = new StringBuilder(argsParaEliminar);
                    argsParaEliminar_Mod.setLength(argsParaEliminar.length() - 2);
                    
                    sqlModificar = "UPDATE " + nombreTabla + " SET " + argsParaEliminar_Mod + " WHERE CARNET LIKE '" + idRegistro + "'";
                    System.out.println("sqlModificar: " + sqlModificar);
                    PreparedStatement psta = conn.prepareStatement(sqlModificar);
                    psta.execute();
                    psta.close();
                    jLabel_Resultados.setText("¬°Se modific√≥ el registro en la Base de Datos exitosamente!");
                } else {
                    // No hay argumentos para eliminar
                    jLabel_Resultados.setText("¬°Error! No se ingres√≥ ning√∫n argumento para modificar.");
                }
                
            } catch (Exception e) {
                jLabel_Resultados.setText("Ocurri√≥ un error al intentar modificar el registro en la DB.");
                System.out.println("Excepci√≥n:");
                e.printStackTrace();
                System.out.println(e.getCause());
            }            
            
        } else {
            jLabel_Resultados.setText("No es posible modificar el registro porque no existe en la BD.");
            System.out.println("No es posible modificar el registro porque no existe en la BD.");
        }
        jTextField_ID.setText("");
        jTextField_Nombre.setText("");
        jTextField_Salon.setText("");
        jTextField_Grado.setText("");
        jTextField_Horario.setText("");
        jTextField_IDMaestro.setText("");
    }//GEN-LAST:event_jButton_ModificarRegistroActionPerformed

